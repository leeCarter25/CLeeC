public class StringExercise {

    public static void main(String[] args) {
        String firString = ("We don't need no education \nWe don't need no thought control");
        System.out.println(firString);

        String str2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(str2);

        String str3 = "In windows, the main drive is usually C:\\";
        System.out.println(str3);

        String str4 = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(str4);


    }
}
